package Aula4.exercicio2M;

public class Counter {
    private int count;

    public Counter() {
        this.count = 1;
    }

    public Counter(int count) {
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
