package Aula4.exercicio4M;

public class Fracao {

    public static double adicionar(double x, double y) {
        return x + y;
    }

    public static double adicionar(double x, int y) {
        return x + y;
    }

    public static double adicionar(int x, double y) {
        return x + y;
    }

    public static double diminuir(double x, double y) {
        return x - y;
    }

    public static double multiplicar(double x, double y) {
        return x * y;
    }

    public static double dividir(double x, double y) {
        return x / y;
    }

}
